<p>While we covered various ways on how to announce the updates of your legal agreements to users (and why this is important), we mentioned how far more important is for the legal agreements to be enforceable.</p>

<p>Legal agreements your website or mobile app can be any of the below:</p>

<ul>
<li>Privacy Policy</li>
<li>Terms and Conditions</li>
<li>Terms of Service</li>
<li>Community Guidelines</li>
<li>And more</li>
<li>One way to make the legal agreements unenforceable is to adopt the so-called browse-wrap technique, where your legal agreements sit in the footer of your website.</li>
</ul>

<p>In a browse-wrap technique, that area of your website would be the only reference to your legal agreements - Privacy Policy, Terms and Conditions and so on.</p>

